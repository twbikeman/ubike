CREATE TABEL ubike (
    id INT NOT NULL AUTO_INCREMENT,
    sno VARCHAR(10),
    sna VARCHAR(10),
    tot INT,
    sbi INT,
    sarea VARCHAR(40),
    mday TIMESTAMP;
    lat FLOAT;
    lng FLOAT;
    sareaen VARCHAR(10),
    snaen VARCHAR(10,)
    aren VARCHAR(10),
    bemp INT,
    act INT,
    PRIMARY KEY(id)
);
